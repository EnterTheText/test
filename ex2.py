from random import uniform as uni
from math import pi
n = 100
nc = 0
for k in range(1, 11):
    for i in range(n):
        if uni(0, 1)**2 + uni(0, 1)**2 <= 1:
            nc += 1
    print(k, n, nc/n * 4, abs(pi - (nc/n * 4)))
    nc = 0
    n *= 10

